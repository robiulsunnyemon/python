Greeting="Good Morning"; #string type
print(type(Greeting));

number= 56777; #integer type
print(type(number));

floatNumber=20.22; #floating type
print(type(floatNumber));

complexNumber=3+2j; #complex type
print(type(complexNumber));

Fruits=["Mango","Banana","Cherry"]; #list type
print(type(Fruits));

Animals=("Cow","Cat","Ant");
print(type(Animals)); #tuple type

Departments={"CSE","EEE","SWE"} #set type
print(type(Departments));

employe={"name":"Emon","age":22}; #dictionary
